# 반복문

# 1. 반복문 for
for a in range(10) :
    print(a)

for a in range(50,100) :
    print(a)

# 2. 반복문 while
b = 0

while b < 10 :
    print(b)
    b += 1

#조건문 
name = "강경민"

if name != "강경민":
    print(name,"이 아닙니다.")
else:
    print(name,"입니다")

num = 20
if num == 10:
    print(num,"은 10입니다.")
elif num == 30:
    print(num,"은 30입니다")
else :
    print(num,"은 10도 30도 아닙니다.")

